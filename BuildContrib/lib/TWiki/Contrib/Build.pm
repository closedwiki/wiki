sub cd {
    my ($this, $file) = @_;

    if ($this->{-v} || $this->{-n}) {
        print 'cd '.$file.$NL;
    }
    if (!$this->{-n}) {
        chdir($file) || die 'Failed to cd to '.$file;
    }
}

