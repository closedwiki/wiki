sub perl_action {
    my ($this, $cmd) = @_;

    if ($this->{-v} || $this->{-n}) {
        print $cmd.$NL;
    }
    unless ($this->{-n}) {
        eval $cmd;
        die 'Failed to '.$cmd.': '.$@ if ($@);
    }
}

